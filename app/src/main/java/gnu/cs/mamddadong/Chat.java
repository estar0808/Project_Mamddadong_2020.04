package gnu.cs.mamddadong;

public class Chat {

    String userName;
    String text;

    public Chat() {}

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
